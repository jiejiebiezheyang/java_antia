public class Demo002 {
    public static void main(String[] args) {

        System.out.println(10 > 5);
        System.out.println(10 >= 5);
        System.out.println(10 < 5);
        System.out.println(10 <= 5);
        System.out.println(10 == 5);
        System.out.println(10 != 5);
        System.out.println();
        System.out.println();
    }
}
